    def test_nexus_request_waits_for_stable_archive_and_recommends_dry_run(self):
        with tempfile.TemporaryDirectory() as td:
            base=Path(td); instance=self.make_instance(base); config=base/"config.toml"
            cli.save_config({"mo2_instance_path":str(instance),"profile":"Default"},config)
            downloads=instance/"downloads"; target=downloads/"Example Mod-123-456.zip"
            metadata={"mod_id":123,"file_id":456,"file_name":target.name,"version":"1.0","category_name":"MAIN","expected_size_bytes":2048}
            calls={"sleep":0}
            def fake_sleep(_seconds):
                calls["sleep"]+=1
                if calls["sleep"]==1: target.write_bytes(b"x"*2048)
            layout={"type":"mo2","has_nesting":False,"has_fomod":False,"has_root_files":False,"plugin_count":1}
            with patch.object(cli,"CONFIG_PATH",config), patch.object(cli,"default_browser_downloads_dir",return_value=downloads), patch.object(cli,"_nexus_file_metadata",return_value=metadata), patch.object(cli,"open_browser_tab",return_value=True), patch.object(cli,"_test_and_inspect_archive",return_value=layout), patch.object(cli.time,"sleep",side_effect=fake_sleep):
                out=io.StringIO()
                with contextlib.redirect_stdout(out): code=cli.main(["nexus","request","123","456","--timeout","5","--poll-interval","0","--json"])
            payload=json.loads(out.getvalue())
            self.assertEqual(code,0); self.assertEqual(payload["data"]["status"],"verified")
            self.assertIn("install",payload["data"]["recommended_commands"][0])
            self.assertIn("install inspect",payload["data"]["recommended_commands"][0])
            self.assertIn("--modid 123 --file-id 456",payload["data"]["recommended_commands"][1])

