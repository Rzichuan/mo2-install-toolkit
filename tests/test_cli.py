    def test_root_deploy_backs_up_and_restores_engine_fixes(self):
        with tempfile.TemporaryDirectory() as td:
            base=Path(td); instance=self.make_instance(base); game=base/"game"; game.mkdir()
            (game/"SkyrimSE.exe").write_bytes(b"game")
            (game/"d3dx9_42.dll").write_bytes(b"old")
            content=base/"content"; content.mkdir()
            (content/"d3dx9_42.dll").write_bytes(b"new")
            (content/"tbb.dll").write_bytes(b"tbb")
            archive=base/"engine-fixes.zip"; archive.write_bytes(b"archive")
            config=base/"config.toml"
            cli.save_config({"mo2_instance_path":str(instance),"profile":"Default","skyrim_game_path":str(game)},config)
            with patch.object(cli,"CONFIG_PATH",config), patch.object(cli,"_extract_root_archive",return_value=content), patch.object(cli,"_game_processes_running",return_value=[]):
                out=io.StringIO()
                with contextlib.redirect_stdout(out): code=cli.main(["root","deploy",str(archive),"--yes","--json"])
                payload=json.loads(out.getvalue()); backup_id=payload["data"]["backup_id"]
                self.assertEqual(code,0); self.assertEqual((game/"d3dx9_42.dll").read_bytes(),b"new")
                code=cli.main(["backup","restore",backup_id,"--yes","--json"])
            self.assertEqual(code,0); self.assertEqual((game/"d3dx9_42.dll").read_bytes(),b"old")
            self.assertFalse((game/"tbb.dll").exists())

